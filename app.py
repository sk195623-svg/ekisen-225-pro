def get_day_info():
    zodiacs = ["子", "丑", "寅", "卯", "辰", "巳", "午", "未", "申", "酉", "戌", "亥"]
    today = datetime.datetime.now()
    today_idx = (today.day + today.month * 2) % 12 
    return zodiacs[today_idx]

